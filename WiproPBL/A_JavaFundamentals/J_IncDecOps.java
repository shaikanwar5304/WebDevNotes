public class J_IncDecOps {
    public static void main(String[] args) {
        int x=2;
        int y=30;
        //pre increment
        System.out.println(++x);//3
        System.out.println(x);//3
        //pre decrement
        System.out.println(--y);//29
        System.out.println(y);//29
        //post increment
        System.out.println(y++);//29
        System.out.println(y);//30
    }
}